# Se desea calcular la conversión a Pesos, sabiendo el valor del dólar blue y la cantidad de Dólares

cantidad_dolares = float(input('Ingrese la cantidad de dólares: '))
valor_dolar_blue = float(input('Ingrese el valor del dólar blue: '))

conversion_a_pesos = cantidad_dolares * valor_dolar_blue

print('El valor en pesos de', cantidad_dolares, 'dólares es de', conversion_a_pesos, 'pesos')